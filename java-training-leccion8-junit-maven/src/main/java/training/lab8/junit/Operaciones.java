package training.lab8.junit;

public class Operaciones {
	
	public int num1;
	public int num2;
	
	public Operaciones(int num1, int num2) {
		this.num1 = num1;
		this.num2 = num2;
	}
	
	public int sumar(int num1, int num2) {		
		return num1 + num2;		
	}
	
	public int restar(int num1, int num2) {
		return num1 - num2;
	}
	
	

}
